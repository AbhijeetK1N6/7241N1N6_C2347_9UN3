/////////////////////////////////////////Printing String Array Using Pointers////////////////////////////////////////////////////
#include<stdio.h>
int main()
{
    char st[]="hello world";
    char *ptos;
    ptos=&st[0];
    int counter=0;
    while(*ptos!= '\0'){
        printf("%c",*ptos);
        ptos++;
        counter+=1;
    }
}


